const { Sequelize, DataTypes, Model } = require('sequelize');
const sequelize = require('../database'); // Importa el objeto sequelize desde el archivo database.js

class User extends Model {}

User.init(
  {
    email: {
      type: DataTypes.STRING,
      allowNull: false,
      unique: true,
    },
    password: {
      type: DataTypes.STRING,
      allowNull: false,
    },
  },
  {
    sequelize,
    modelName: 'User',
    tableName: 'users',
    timestamps: true,
  }
);

module.exports = User;


